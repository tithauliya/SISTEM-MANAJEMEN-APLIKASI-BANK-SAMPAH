    private void jButtonTARIKSALDOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTARIKSALDOActionPerformed
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("BANK_SAMPAHPU");
        EntityManager em = emf.createEntityManager();

        try {
            if (jTextFieldJUMLAHPENARIKAN.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Masukkan jumlah penarikan!");
                return;
            }
            if (jComboBoxPILIHMETODE.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Pilih metode penarikan dulu!");
                return;
            }

            int jumlahTarik = Integer.parseInt(jTextFieldJUMLAHPENARIKAN.getText());

            if (jumlahTarik < 10000) {
                JOptionPane.showMessageDialog(this, "Minimal penarikan Rp 10.000!");
                return;
            }

            em.getTransaction().begin();

            Nasabah n = em.find(Nasabah.class, nasabah.getIdNasabah());
            em.refresh(n); // Pastikan data saldo paling update

            if (n == null || n.getSaldo() == null) {
                JOptionPane.showMessageDialog(this, "Data Saldo nasabah tidak ditemukan!");
                em.getTransaction().rollback();
                return;
            }

            BigInteger saldoSaatIni = n.getSaldo().getTotalSaldo();
            BigInteger jumlahTarikBI = BigInteger.valueOf(jumlahTarik);

            if (saldoSaatIni.compareTo(jumlahTarikBI) < 0) {
                java.text.NumberFormat kursIDR = java.text.NumberFormat.getCurrencyInstance(new java.util.Locale("id", "ID"));
                JOptionPane.showMessageDialog(this,
                        "Saldo tidak mencukupi!\nSaldo Anda: " + kursIDR.format(saldoSaatIni));
                em.getTransaction().rollback();
                return;
            }

            String inputPin = JOptionPane.showInputDialog(this, "Masukkan PIN Anda untuk konfirmasi:");
            if (inputPin == null) {
                em.getTransaction().rollback();
                return; // User klik Cancel
            }

            if (!inputPin.trim().equals(n.getPasswordNasabah().trim())) {
                JOptionPane.showMessageDialog(this, "PIN salah! Transaksi dibatalkan.");
                em.getTransaction().rollback();
                return;
            }

            BigInteger saldoBaru = saldoSaatIni.subtract(jumlahTarikBI);
            Saldo saldoEntity = n.getSaldo();
            saldoEntity.setTotalSaldo(saldoBaru);

            em.merge(saldoEntity);

            Penarikan p = new Penarikan();
            p.setTanggal(new java.util.Date());
            p.setJumlah(jumlahTarik);
            p.setMetode(jComboBoxPILIHMETODE.getSelectedItem().toString());
            p.setIdNasabah(n);

            em.persist(p);

            em.getTransaction().commit();

            java.text.NumberFormat kursIDR = java.text.NumberFormat.getCurrencyInstance(new java.util.Locale("id", "ID"));
            JOptionPane.showMessageDialog(this,
                    "Penarikan Berhasil!\nSisa Saldo: " + kursIDR.format(saldoBaru));

            jLabel3.setText(kursIDR.format(saldoBaru));

            jTextFieldJUMLAHPENARIKAN.setText("");
            jComboBoxPILIHMETODE.setSelectedIndex(0);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka valid!");
        } catch (Exception e) {
            if (em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace();
        } finally {
            if (em != null) {
                em.close();
            }
            if (emf != null) {
                emf.close();
            }
        }
    }//GEN-LAST:event_jButtonTARIKSALDOActionPerformed

