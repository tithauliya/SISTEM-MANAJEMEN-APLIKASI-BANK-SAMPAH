            jviewer.setSize(800, 600);
            jviewer.setLocationRelativeTo(null); // Tampil di tengah layar
            jviewer.setTitle("Struk Penarikan");

            // --- INI KUNCINYA AGAR MUNCUL DI DEPAN ---
