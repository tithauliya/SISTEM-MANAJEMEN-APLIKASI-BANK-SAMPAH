    public void bersih() {
        textIDJENISSAMPAH.setText("");
        textNAMAsampah.setText("");
        jComboBoxKategori.setSelectedIndex(0);
        textHARGAPERKG.setText("");
    }

    private boolean validasiInput() {
        if (textNAMAsampah.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama sampah harus diisi");
            textNAMAsampah.requestFocus();
            return false;
        }
        if (jComboBoxKategori.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Kategori harus dipilih");
            jComboBoxKategori.requestFocus();
            return false;
        }
        String hargaText = textHARGAPERKG.getText().trim();
        if (!hargaText.isEmpty()) {
            try {
                new BigInteger(hargaText);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Harga per KG harus berupa angka");
                textHARGAPERKG.requestFocus();
                return false;
            }
        }
        return true;
    }

    private Integer getNextIdJenisSampah() {
        try {
            Integer next = em.createQuery(
                    "SELECT COALESCE(MAX(j.idJenissampah), 0) + 1 FROM JenisSampah j", Integer.class)
                    .getSingleResult();
            return next;
        } catch (Exception ex) {
            return 1;
        }
    }

