    private void jButtonSIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSIMPANActionPerformed
        try {
            if (!validasiInput()) {
                return;
            }
            if (em == null || !em.isOpen()) {
                JOptionPane.showMessageDialog(this, "Koneksi belum siap.");
                return;
            }

            String idText = textIDJENISSAMPAH.getText().trim();
            String nama = textNAMAsampah.getText().trim();
            String kategori = jComboBoxKategori.getSelectedItem().toString();
            String hargaText = textHARGAPERKG.getText().trim();

            em.getTransaction().begin();
            JenisSampah js = new JenisSampah();

            if (!idText.isEmpty()) {
                js.setIdJenissampah(Integer.parseInt(idText));
            } else {
                js.setIdJenissampah(getNextIdJenisSampah());
            }

            js.setNamaSampah(nama);
            js.setKategori(kategori);
            if (!hargaText.isEmpty()) {
                js.setHargaPerKg(new BigInteger(hargaText));
            }

            em.persist(js);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            this.dispose();
            bersih();

        } catch (Exception e) {
            if (em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButtonSIMPANActionPerformed

