    private boolean validasiInput() {
        if (textIDPETUGAS.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID Petugas harus diisi");
            textIDPETUGAS.requestFocus();
            return false;
        }
        if (textNAMAPETUGAS.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Petugas harus diisi");
            textNAMAPETUGAS.requestFocus();
            return false;
        }
        if (textNAMAPENGGUNAPETUGAS.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama pengguna harus diisi");
            textNAMAPENGGUNAPETUGAS.requestFocus();
            return false;
        }
        if (textKATASANDIPETUGAS.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kata sandi harus diisi");
            textKATASANDIPETUGAS.requestFocus();
            return false;
        }
        return true;
    }

    public int autoID() {
        int id = 1; // Default ID awal
        try {
            // Ambil ID Petugas terbesar (MAX) dari database
            Integer maxID = em.createQuery(
                    "SELECT MAX(p.idPetugas) FROM Petugas p", Integer.class)
                    .getSingleResult();

            if (maxID != null) {
                id = maxID + 1; // Tambahkan 1 dari ID terakhir
            }
        } catch (Exception e) {
            System.out.println("Tabel kosong atau error: " + e.getMessage());
        }
        return id;
    }

