    private void jButtonSIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSIMPANActionPerformed

        try {
            // Validasi Input Kosong (Kecuali ID, karena ID otomatis)
            if (textNAMAPETUGAS.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Isi Nama Petugas!");
            } else if (textNAMAPENGGUNAPETUGAS.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Isi Username Petugas!");
            } else if (textKATASANDIPETUGAS.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Isi Password Petugas!");
            } else if (textNAMAIBUPETUGAS.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Isi Nama Ibu!");
            } else {

                // Mulai Transaksi
                EntityManager em = emf.createEntityManager();
                em.getTransaction().begin();

                Petugas p = new Petugas();

                // === PERBAIKAN PENTING DI SINI ===
                // Jangan ambil dari Text Field, tapi panggil fungsi autoID() lagi 
                // untuk memastikan ID benar-benar baru dan unik saat detik penyimpanan.
                p.setIdPetugas(autoID());
                // =================================

                p.setNamaPetugas(textNAMAPETUGAS.getText());
                p.setUsernamePetugas(textNAMAPENGGUNAPETUGAS.getText());
                p.setPasswordPetugas(textKATASANDIPETUGAS.getText());
                p.setNamaIbu(textNAMAIBUPETUGAS.getText());

                em.persist(p);
                em.getTransaction().commit();

                // Tutup EntityManager
                em.close();

                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");

                bersih();
                dispose(); // Tutup form tambah
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Simpan gagal: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonSIMPANActionPerformed

