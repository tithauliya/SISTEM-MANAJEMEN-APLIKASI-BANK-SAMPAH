    private void jToggleJUALActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleJUALActionPerformed

        try {
            // ===========================
            // 1. VALIDASI INPUT
            // ===========================
            if (jComboBoxNAMAMITRA.getSelectedItem() == null
                    || jComboBoxHASILPENGOLAHAN.getSelectedItem() == null
                    || jTextBERATPENJUALAN.getText().trim().isEmpty()
                    || jTextTOTALHARGA.getText().trim().isEmpty()) {

                JOptionPane.showMessageDialog(this, "Semua data (Mitra, Produk, Berat, Total) harus dilengkapi!");
                return;
            }

            // ===========================
            // 2. AMBIL DATA DARI FORM
            // ===========================
            MitraJual mitra = (MitraJual) jComboBoxNAMAMITRA.getSelectedItem();
            Pengolahan pengolahanDipilih = (Pengolahan) jComboBoxHASILPENGOLAHAN.getSelectedItem();
            JenisSampah jenisProduk = pengolahanDipilih.getIdJenissampah();

            // Parsing Angka
            BigInteger beratJual = new BigInteger(jTextBERATPENJUALAN.getText());
            BigInteger totalHarga = new BigInteger(jTextTOTALHARGA.getText());
            Date tanggal = new SimpleDateFormat("yyyy-MM-dd").parse(jTextTANGGALPENJUALAN.getText());

            // ===========================
            // 3. CEK STOK GUDANG 
            // ===========================
            StokGudang stokBarangJadi = null;
            // Query to find stock for the specific waste type with status "Hasil Pengolahan"
            List<StokGudang> daftarStok = em.createQuery(
                    "SELECT s FROM StokGudang s WHERE s.idJenissampah.idJenissampah = :id "
                    + "AND s.statusStok = :status AND s.beratTersedia > 0", StokGudang.class)
                    .setParameter("id", jenisProduk.getIdJenissampah())
                    .setParameter("status", "Hasil Pengolahan")
                    .getResultList();

            if (daftarStok.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Stok Kosong for " + jenisProduk.getNamaSampah());
                return;
            }

            int beratJualInt = beratJual.intValue();

            // Find a batch with enough stock
            for (StokGudang s : daftarStok) {
                if (s.getBeratTersedia() >= beratJualInt) {
                    stokBarangJadi = s;
                    break;
                }
            }

            if (stokBarangJadi == null) {
                JOptionPane.showMessageDialog(this, "Stok tidak mencukupi for this transaction!");
                return;
            }

            // ===========================
            // 4. PROSES SIMPAN (FIX DUPLICATE ID)
            // ===========================
            em.getTransaction().begin();

            PenjualanMitra p = new PenjualanMitra();

            // --- PERBAIKAN UTAMA DISINI ---
            // Always generate a NEW ID. Do not rely on the text field which might show an old ID.
            int idBaru = autoID();
            p.setIdPenjualanMitra(idBaru);
            // ------------------------------

            p.setTanggalPenjualanMitra(tanggal);
            p.setBeratKgPenjualanMitra(beratJual);
            p.setTotalHargaPenjualanMitra(totalHarga);
            p.setIdMitra(mitra);
            p.setIdPengolahan(pengolahanDipilih);

            em.persist(p);

            // Update Stok
            int sisa = stokBarangJadi.getBeratTersedia() - beratJualInt;
            stokBarangJadi.setBeratTersedia(sisa);

            // If stock becomes 0, change status (optional but good practice)
            if (sisa <= 0) {
                stokBarangJadi.setStatusStok("Terjual");
            }
            em.merge(stokBarangJadi);

            // ================================================================
            // C. UPDATE KAS BANK (BERTAMBAH / UANG MASUK)
            // ================================================================
            // Ambil Dompet Bank ID 1
            DompetBankSampah_1 dompet = em.find(DompetBankSampah_1.class, 1);

            if (dompet != null) {
                // Refresh data biar terbaru
                em.refresh(dompet);

                // Konversi BigInteger (Form) ke long (Entity)
                long hargaJualLong = totalHarga.longValue();

                // Rumus: Saldo Lama + Pendapatan Baru
                long saldoBaru = dompet.getTotalKasBank() + hargaJualLong;

                // Set saldo baru
                dompet.setTotalKasBank(saldoBaru);
                dompet.setTerakhirDiupdate(new Date());

                em.merge(dompet);
            } else {
                // Jika dompet belum ada, bisa pilih mau error atau buat baru
                // Disini saya buatkan peringatan saja agar admin isi modal dulu
                JOptionPane.showMessageDialog(this, "Peringatan: Dompet Bank ID 1 tidak ditemukan. Saldo tidak bertambah.");
            }
            // ================================================================

            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Penjualan Berhasil! Stok berkurang & Kas Bank bertambah.");

            bersih();

            // Tampilkan ID selanjutnya di text field untuk info user (visual feedback only)
            jTextIDPENGOLAHAN.setText(String.valueOf(autoID()));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
            if (em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            e.printStackTrace();
        }

    }//GEN-LAST:event_jToggleJUALActionPerformed

