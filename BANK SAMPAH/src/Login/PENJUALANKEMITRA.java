    private int autoID() {
        int id = 1; // Default ID awal jika tabel kosong
        try {
            // Ambil ID terbesar (MAX) dari tabel PenjualanMitra
            // Bukan ambil semua list, tapi cukup 1 angka terbesar saja
            Integer maxID = em.createQuery(
                    "SELECT MAX(p.idPenjualanMitra) FROM PenjualanMitra p", Integer.class)
                    .getSingleResult();

            if (maxID != null) {
                id = maxID + 1; // Tambahkan 1 dari ID terakhir
            }
        } catch (Exception e) {

        }
        return id;
    }

