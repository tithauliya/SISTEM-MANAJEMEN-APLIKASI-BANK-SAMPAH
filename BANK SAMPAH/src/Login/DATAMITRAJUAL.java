    private void jButtonEDITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEDITActionPerformed
        int row = TableMITRAJUAL.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data dulu di tabel!");
            return;
        }

        Integer id = Integer.parseInt(TableMITRAJUAL.getValueAt(row, 0).toString());
        String nama = TableMITRAJUAL.getValueAt(row, 1).toString();
        String alamat = TableMITRAJUAL.getValueAt(row, 2).toString();
        String noTelp = TableMITRAJUAL.getValueAt(row, 3).toString();
        String jenisKerjaSama = TableMITRAJUAL.getValueAt(row, 4).toString();

        editMITRAJUAL dialog = new editMITRAJUAL(this, true, id, nama, alamat, noTelp, jenisKerjaSama);
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);

        showTable();

    }//GEN-LAST:event_jButtonEDITActionPerformed

