    private void jButtonHAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHAPUSActionPerformed
        int[] rows = TableMITRAJUAL.getSelectedRows(); // ambil semua baris yang dipilih
        if (rows.length == 0) {
            JOptionPane.showMessageDialog(this, "Pilih data Mitra Jual dulu di tabel!");
            return;
        }

        String[] idMitraLama = new String[rows.length];
        String[] namaMitraLama = new String[rows.length];
        String[] alamatLama = new String[rows.length];
        String[] noTelpLama = new String[rows.length];

        for (int i = 0; i < rows.length; i++) {
            idMitraLama[i] = TableMITRAJUAL.getValueAt(rows[i], 0).toString();   // kolom ID
            namaMitraLama[i] = TableMITRAJUAL.getValueAt(rows[i], 1).toString(); // kolom Nama Mitra
            alamatLama[i] = TableMITRAJUAL.getValueAt(rows[i], 2).toString();    // kolom Alamat
            noTelpLama[i] = TableMITRAJUAL.getValueAt(rows[i], 3).toString();    // kolom No. Telp
        }

        hapusMITRAJUAL dialog = new hapusMITRAJUAL(this, true, idMitraLama, namaMitraLama, alamatLama, noTelpLama);
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);
        showTable();


    }//GEN-LAST:event_jButtonHAPUSActionPerformed

