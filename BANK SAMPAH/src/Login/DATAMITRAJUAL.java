    public void showTable() {
        try {
            em.getEntityManagerFactory().getCache().evictAll();
            em.clear();

            List<MitraJual> hasil = em.createNamedQuery("MitraJual.findAll", MitraJual.class)
                    .getResultList();

            DefaultTableModel model = new DefaultTableModel(
                    new String[]{
                        "ID Mitra", "Nama Mitra", "Alamat", "Telpon", "Jenis Kerja Sama"
                    }, 0
            );

            for (MitraJual m : hasil) {
                model.addRow(new Object[]{
                    m.getIdMitra(),
                    m.getNamaMitra(),
                    m.getAlamatMitra(),
                    m.getTelponMitra(),
                    m.getJenisKerjaSama()
                });
            }

            TableMITRAJUAL.setModel(model);

            DefaultTableCellRenderer center = new DefaultTableCellRenderer();
            center.setHorizontalAlignment(JLabel.CENTER);
            for (int i = 0; i < TableMITRAJUAL.getColumnCount(); i++) {
                TableMITRAJUAL.getColumnModel().getColumn(i).setCellRenderer(center);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
        }
    }

